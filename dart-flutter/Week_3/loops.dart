  var a = 1;
  var maxnum = 11;
  while (a < maxnum) {
    // it will print until the expression return false
    print(a);
    a = a + 1; // increase value 1 after each iteration
  }

//The code prints numbers from 1 to 10 using a while loop in Dart.
}
